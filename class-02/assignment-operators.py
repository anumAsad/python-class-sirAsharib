x = 5
x += 3  # Equivalent to x = x + 3
print(x)  # 8

x -= 2  # Equivalent to x = x - 2
print(x)  # 6

x *= 4  # Equivalent to x = x * 4
print(x)  # 24

x /= 3  # Equivalent to x = x / 3
print(x)  # 8.0

x //= 2  # Equivalent to x = x // 2
print(x)  # 4.0

x %= 3  # Equivalent to x = x % 3
print(x)  # 1.0

x **= 2  # Equivalent to x = x ** 2
print(x)  # 1.0
